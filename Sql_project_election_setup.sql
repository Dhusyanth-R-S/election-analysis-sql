
CREATE DATABASE polity;
USE polity;


CREATE TABLE states (
    state_id INT PRIMARY KEY,
    state_name VARCHAR(50) NOT NULL
);
CREATE TABLE districts (
    district_id INT PRIMARY KEY,
    district_name VARCHAR(50) NOT NULL,
    state_id INT NOT NULL,
    FOREIGN KEY (state_id) REFERENCES states(state_id)
);
CREATE TABLE constituencies (
    constituency_id INT PRIMARY KEY,
    constituency_name VARCHAR(50) NOT NULL,
    district_id INT NOT NULL,
    FOREIGN KEY (district_id) REFERENCES districts(district_id)
);
CREATE TABLE parties (
    party_id INT PRIMARY KEY,
    party_name VARCHAR(50) NOT NULL,
    leader_name VARCHAR(50) NOT NULL
);
CREATE TABLE candidates (
    candidate_id INT PRIMARY KEY,
    candidate_name VARCHAR(50) NOT NULL,
    age INT NOT NULL CHECK (age BETWEEN 25 AND 90),
    gender VARCHAR(10) NOT NULL CHECK (gender IN ('Male','Female')),
    constituency_id INT NOT NULL,
    party_id INT NOT NULL,
    FOREIGN KEY (constituency_id) REFERENCES constituencies(constituency_id),
    FOREIGN KEY (party_id) REFERENCES parties(party_id)
);
CREATE TABLE voters (
    voter_id INT PRIMARY KEY,
    voter_name VARCHAR(50) NOT NULL,
    age INT NOT NULL CHECK (age BETWEEN 18 AND 120),
    gender VARCHAR(10) NOT NULL CHECK (gender IN ('Male','Female')),
    constituency_id INT NOT NULL,
    FOREIGN KEY (constituency_id) REFERENCES constituencies(constituency_id)
);
CREATE TABLE Votes (
    vote_id INT PRIMARY KEY,
    voter_id INT,
    candidate_id INT,
    FOREIGN KEY (voter_id) REFERENCES Voters(voter_id),
    FOREIGN KEY (candidate_id) REFERENCES candidates(candidate_id)
);

